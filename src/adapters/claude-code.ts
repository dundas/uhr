import path from "node:path";
import type { HookDeclaration } from "../types";
import type { Adapter, AdapterWarning } from "./types";

const EVENT_MAP: Record<string, string | undefined> = {
  beforeToolExecution: "PreToolUse",
  afterToolExecution: "PostToolUse",
  stop: "Stop",
  sessionStart: "SessionStart",
  notification: "Notification",
  beforePromptSubmit: "UserPromptSubmit",
  permissionRequest: "PermissionRequest",
  subagentStop: "SubagentStop",
  beforeCompact: "PreCompact"
};

const TOOL_MAP: Record<string, string> = {
  write: "Write",
  edit: "Edit",
  "multi-edit": "MultiEdit",
  bash: "Bash",
  read: "Read",
  grep: "Grep",
  fetch: "WebFetch"
};

function matcherForTools(tools?: string[]): string {
  if (!tools || tools.length === 0 || tools.includes("*")) {
    return "";
  }

  const mapped = tools
    .map((tool) => TOOL_MAP[tool])
    .filter((value): value is string => Boolean(value))
    .sort();

  if (mapped.length === 0) {
    return "";
  }

  return mapped.join("|");
}

function findHookByRef(ref: string, installed: Record<string, { hooks: HookDeclaration[] }>): HookDeclaration | null {
  const [serviceName, hookId] = ref.split("/");
  if (!serviceName || !hookId) {
    return null;
  }
  const service = installed[serviceName];
  if (!service) {
    return null;
  }
  return service.hooks.find((hook) => hook.id === hookId) ?? null;
}

export const claudeCodeAdapter: Adapter = {
  id: "claude-code",
  name: "Claude Code",
  detect: () => true,
  generate: (lockfile, cwd) => {
    const warnings: AdapterWarning[] = [];
    const hooks: Record<string, unknown[]> = {};

    for (const [eventName, refs] of Object.entries(lockfile.resolvedOrder)) {
      const platformEvent = EVENT_MAP[eventName];

      if (!platformEvent) {
        for (const ref of refs) {
          warnings.push({
            hookId: ref,
            message: `No Claude Code mapping for universal event: ${eventName}`
          });
        }
        continue;
      }

      if (!hooks[platformEvent]) {
        hooks[platformEvent] = [];
      }

      for (const ref of refs) {
        const hook = findHookByRef(ref, lockfile.installed);
        if (!hook) {
          continue;
        }

        hooks[platformEvent].push({
          matcher: matcherForTools(hook.tools),
          _uhrSource: ref,
          hooks: [
            {
              type: "command",
              command: hook.command
            }
          ]
        });
      }
    }

    const allowedTools = Array.from(
      new Set(
        Object.values(lockfile.installed).flatMap((service) => service.permissions?.allow ?? [])
      )
    ).sort();

    return {
      filepath: path.join(cwd, ".claude", "settings.json"),
      content: {
        _managedBy: "uhr",
        _generatedAt: lockfile.generatedAt,
        _warning: "Auto-generated by UHR. Do not edit.",
        hooks,
        permissions: {
          allowedTools
        }
      },
      warnings
    };
  }
};
